# 已知点
x1 = 11
x2 = 12
y1 = 2.3979  # ln(11)
y2 = 2.4849  # ln(12)

# 要估计的点
x = 11.75

# 线性插值公式
y = y1 + (x - x1) * (y2 - y1) / (x2 - x1)

print("使用线性插值得到的 ln(11.75) 的近似值为:", y)

